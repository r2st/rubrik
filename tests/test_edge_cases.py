# ---------------------------------------------------------------------------
# Internal-meeting + customer name mention (case 8)
# ---------------------------------------------------------------------------
def test_internal_strategy_classifies_as_internal_not_external():
    """Customer name in the title doesn't make it an external meeting if
    the prefix isn't 'Aegis /' or 'URGENT:'."""
    df = _load_one("internal_mentions_customer", "account_strategy_internal")
    row = df.iloc[0]
    assert row["call_type"] == "internal", \
        f"internal strategy mis-classified as {row['call_type']!r}"
    assert row["customer"] is None, \
        f"customer extracted from internal meeting title: {row['customer']!r}"


# ---------------------------------------------------------------------------
# End-to-end smoke: load ALL synthetic fixtures, run the full pipeline
# ---------------------------------------------------------------------------
def test_all_synthetic_fixtures_load_and_categorize():
    """Bulk smoke test — every fixture loads, categorizes, and produces a
    valid trajectory. Catches accidental regressions that affect a class of
    inputs we didn't write a specific test for."""
    meeting_dirs = list(FIXTURES_ROOT.rglob("meeting-info.json"))
    assert len(meeting_dirs) >= 8, "expected at least 8 synthetic fixtures"

    meetings = [data_loader.load_meeting(p.parent) for p in meeting_dirs]
    df = data_loader.meetings_to_dataframe(meetings)
    df = categorizer.annotate(df)
    sentences = data_loader.sentences_dataframe(meetings)
    df = sentiment.add_trajectories(df, sentences)

    # Every row gets a categorization
    assert df["call_type"].isin(["support", "external", "internal"]).all()
    assert df["meeting_purpose"].notna().all()
    assert df["product_areas"].apply(lambda x: len(x) > 0).all()
    # Every row gets a 5-bucket trajectory
    assert df["trajectory"].apply(lambda t: isinstance(t, list) and len(t) == 5).all()
