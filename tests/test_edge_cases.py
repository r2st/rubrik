def test_truly_unknown_product_lands_in_general():
    """Sanity check: a meeting referencing no known product keyword lands in
    the 'General' fallback bucket rather than silently disappearing."""
    df = _load_one("internal_mentions_customer", "account_strategy_internal")
    # The strategy meeting mentions Northstar Pharma + SentinelShield (a
    # competitor); neither matches our product-area keywords.
    assert df.iloc[0]["product_areas"] == ["General"]


# ---------------------------------------------------------------------------
# All-neutral long meeting (case 4) — verify trajectory math doesn't fire
# false friction signals
# ---------------------------------------------------------------------------
def test_all_neutral_meeting_has_zero_max_drop():
    df = _load_one("all_neutral", "weekly_status")
    sentences = data_loader.sentences_dataframe([
        data_loader.load_meeting(FIXTURES_ROOT / "all_neutral" / "weekly_status")
    ])
    df = sentiment.add_trajectories(df, sentences)
    row = df.iloc[0]
    # All neutral → trajectory all zeros → max_drop should be 0
    assert row["max_drop"] == 0.0, \
        f"flat meeting flagged a friction moment: max_drop={row['max_drop']}"
    assert row["share_negative"] == 0.0


# ---------------------------------------------------------------------------
# Single-sentence meeting (case 5) — verify bucket math doesn't crash
# ---------------------------------------------------------------------------
def test_single_sentence_does_not_crash():
    """Pipeline must not blow up on a meeting with N < n_buckets sentences."""
    df = _load_one("single_sentence", "quick_sync")
    sentences = data_loader.sentences_dataframe([
        data_loader.load_meeting(FIXTURES_ROOT / "single_sentence" / "quick_sync")
    ])
    df = sentiment.add_trajectories(df, sentences)
    row = df.iloc[0]
    # Trajectory should still be 5 buckets long
    assert isinstance(row["trajectory"], list)
    assert len(row["trajectory"]) == 5
    # No NaN poisoning of downstream signals
    assert not pd.isna(row["max_drop"])


