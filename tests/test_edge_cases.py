"""Verify the pipeline handles synthetic edge cases described in
`docs/edge-cases.md` and generated by `tests/fixtures/synthetic/gen_synthetic.py`.

Each test loads a single edge-case fixture, runs the relevant pipeline stage,
and asserts the *intended* behavior — what we want a regression check to
catch if the rules / categorizer / sentiment math drift.
"""
from __future__ import annotations

from pathlib import Path

import pandas as pd
import pytest

from src import categorizer, data_loader, sentiment

FIXTURES_ROOT = Path(__file__).parent / "fixtures" / "synthetic"


def _load_one(category: str, slug: str) -> pd.DataFrame:
    """Load a single synthetic fixture directory through the real loader."""
    fixture_dir = FIXTURES_ROOT / category / slug
    assert fixture_dir.exists(), f"missing fixture: {fixture_dir}"
    meeting = data_loader.load_meeting(fixture_dir)
    df = data_loader.meetings_to_dataframe([meeting])
    return categorizer.annotate(df)


# ---------------------------------------------------------------------------
# Title-format heterogeneity (case 1)
# ---------------------------------------------------------------------------
@pytest.mark.parametrize("slug,expected_call_type,expected_purpose", [
    # Lowercase URGENT — case-insensitive regex catches it
    ("urgent_lowercase",        "external", "Incident Response"),
    # Compound prefix — fixed by widening the regex from `^urgent:` to
    # `^(urgent|escalation)\b.*?:` (this synthetic fixture caught the gap)
    ("multi_word_escalation",   "external", "Incident Response"),
    # Multi-segment Aegis path — separator-spaces fix lets this still match
    ("nested_aegis",            "external", "Renewal/Contract"),
    # Aegis without "/" separator — correctly falls back to internal
    ("aegis_no_separator",      "internal", "Planning/Strategy"),
    # Em-dash separator + "Post-Incident Review" — "Incident" word triggers
    # Incident Response *before* the Review rule. This is the correct order:
    # an "incident review" meeting is fundamentally about an incident.
    ("trailing_punctuation",    "external", "Incident Response"),
])
def test_title_variant_categorization(slug, expected_call_type, expected_purpose):
    df = _load_one("title_variants", slug)
    row = df.iloc[0]
    assert row["call_type"] == expected_call_type, \
        f"{slug}: got call_type={row['call_type']!r}"
    assert row["meeting_purpose"] == expected_purpose, \
        f"{slug}: got meeting_purpose={row['meeting_purpose']!r}"


